class Car:
    def move(self):
        print("Driving 🚗")

class Plane:
    def move(self):
        print("Flying ✈️")

class Dog:
    def move(self):
        print("Running 🐕")

class Fish:
    def move(self):
        print("Swimming 🐟")

# Create instances of each class
car = Car()
plane = Plane()
dog = Dog()
fish = Fish()

# Call the move method on each instance
car.move()
plane.move()
dog.move()
fish.move()